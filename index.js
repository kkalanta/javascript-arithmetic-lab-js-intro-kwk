function add(a, b){
  return a + b;
}

function subtract(a, b){
  return a - b;
}

function multiply(a, b){
  return a * b;
}

function divide(a, b){
  return a / b; 
}

function inc(n){
  return n+= 1;
}

function dec(n){
  return n-= 1;
}

function makeInt(n){
  if (n = parseInt('n', 10)){
    return n;
  if (n === ""){
    return NaN; 
  }
  }
}

function preserveDecimal(n){
  if (n = parseFloat(n)){
    return n; 
  if (n === ""){
    return NaN; 
  }
  }
}
